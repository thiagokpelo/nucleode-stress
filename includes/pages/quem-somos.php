              A clínica conta com profissionais especializados no tratamento da depressão, ansiedade, avaliação para cirurgia bariátrica, obesidade, transtorno bipolar, transtornos alimentares, dependências, treinamento em gerenciamento de estresse e resiliência. A Avaliação neuropsicológica  também faz parte das especialidades da clinica.
              <br>
              No Núcleo de-stress, o planejamento das sessões é feito para cada caso, de acordo com as avaliações iniciais, e direcionado para a flexibilidade psicológica, a saúde emocional e às ações para uma vida plena. 
