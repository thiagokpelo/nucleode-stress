                <p>Resiliência para vida é um programa interativo que oferece a adultos (a partir de 17 anos) habilidades de lidar de forma efetiva com situações desafiantes encontradas ao longo da vida. É estruturado e embasado cientificamente, criado pela Dra Paula Barrett (da clínica Pathways- Austrália). É baseado no programa FRIENDS, reconhecido como eficaz na prevenção de ansiedade e depressão pela Organização Mundial de Saúde. Agrega estratégias da Psicologia Cognitiva e da Psicologia Positiva.</p>

