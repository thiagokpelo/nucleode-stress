                <p>
                  É uma técnica que consiste em medir algumas reações corporais do estresse (temperatura, sudorese e batimentos cardíacos) através de eletrodos colocados na mão do paciente. Eles captam o nível de tensão da pessoa e transmitem para o aparelho de Biofeedback que transforma as informações em imagens gráficas enviando-as ao computador para assim, serem visualizadas.   São ensinadas técnicas de auto-controle que interferem diretamente na reação corporal (batimento cardíaco, sudorese, tensão, temperatura), quebrando o ciclo de estresse. Ele é usado com sucesso em vários tipos de problemas: controle da ansiedade, gerenciamento de estresse, depressão, prevenção de infarto, etc.
