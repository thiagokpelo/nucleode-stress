              <h3 class="title">Estresse</h3>
                <p>É uma modalidade breve de treino em que o paciente é orientado a gerenciar adequadamente seu estresse.</p>

                <p>É feita a avaliação do nível e do tipo de estresse no início e no final do tratamento para constatarmos a evolução.</p>

                <p>O gerenciamento é importante porque, dependendo da fase de estresse que a pessoa está, pode comprometer sua saúde e sua qualidade de vida. Além de diagnosticar e tratar os sintomas do estresse atual, o paciente é orientado a alcançar níveis aceitáveis de estresse no futuro.</p>

                <p>O estresse é uma resposta de adaptação do organismo frente a situações de desafio ou ameaça real ou imaginária, envolve mente, fisiologia e hormônios e prepara para comportamento de luta ou fuga. Sendo assim, não é doença e é necessário para nossa sobrevivência. O problema é quando ele se torna crônico, colocando a saúde em risco e desencadeando alguns sintomas:</p>
